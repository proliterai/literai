/**
 * Один набор промптов для командной игры.
 * Включает три различных промпта для разных ролей в повествовании.
 */
export type TeamPromptSet = {
  /** Уникальный идентификатор набора */
  id: string;
  /** Название набора (например, "Дефолтный", "Боевик", "Юмор") */
  name: string;
  /** Иконка для отображения в интерфейсе */
  icon?: string;
  /** Краткое описание набора */
  description?: string;
  /**
   * Промпт для рассказчика в начале истории (вступление).
   * Используется при старте новой сессии или новой части.
   */
  intro: string;
  /**
   * Промпт для отыгрыша конкретного персонажа.
   * Должен содержать плейсхолдер {CharacterName} для подстановки имени.
   */
  characterActing: string;
  /**
   * Промпт для рассказчика для продолжения истории
   * (после действий персонажей).
   */
  continuation: string;
};

/**
 * Состояние хранилища системных промптов для командного режима.
 */
export type TeamSystemPromptState = {
  /** Загружены ли пресеты (после загрузки из JSON) */
  loaded: boolean;
  /** Массив предустановленных наборов (из файла team-prompts.json) */
  sets: TeamPromptSet[];
  /** Массив пользовательских наборов (сохранённых пользователем) */
  customPresets: TeamPromptSet[];
  /** ID активного в данный момент набора */
  activeSetId: string;
  /**
   * Пользовательские изменения предустановленных наборов.
   * Ключ — ID набора, значение — изменённый набор.
   * Если для набора нет записи, используется оригинал из `sets` или `customPresets`.
   */
  customSets: Record<string, TeamPromptSet>;
};

/**
 * Формат данных для экспорта/импорта настроек промптов.
 * Используется при сохранении сессии или экспорте всего чата.
 */
export type TeamSystemPromptExportData = {
  /** ID активного набора на момент экспорта */
  activeSetId: string;
  /** Пользовательские изменения наборов */
  customSets: Record<string, TeamPromptSet>;
  /** Дата и время экспорта */
  exportedAt: string;
};